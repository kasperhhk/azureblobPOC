            if (CloudStorageAccount.TryParse(connStr, out CloudStorageAccount account))
            {
                var client = account.CreateCloudBlobClient();
                var container = client.GetContainerReference("kkr");
                var blob = container.GetBlockBlobReference(file.FileName);
